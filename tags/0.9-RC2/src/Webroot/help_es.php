<h2>FAQ</h2>
<dl>
<dt>1. ¿Por qué una nueva UI?</dt>
	<dd><p><b>Razón 1:</b> Deseaba hacer algo distinto y guay. La IU web es <i>in</i>. La GUI de Windows es <i>out</i>.</p>
	<p><b>Razón 2:</b> Es más práctico. Mi trabajo requiere que escriba librerías y código de sistemas más que de interfaces de usuario (GUI). La programación en Windows (o para este problema: la programación del GUI) no ha resultado sencilla a la hora de separar la lógica de la presentación. Durante <i>largo</i> tiempo, he tratado de abordar una manera de resolver este problema. ¿El resultado final? Este.</p></dd>
<dt>2. ¿Por qué debo de usar mi navegador? ¿Mis datos permanecen protegidos?</dt>
	<dd><p>No estas accediendo a internet. Todo se realiza de manera local. Yammy ejecuta un servidor web ligero. Tu información se encuentra solo en tu pc. Los usuarios <b>no pueden</b> acceder a Yammy desde una máquina remota.</p>
	<p>¿Y el navegador? Es usado para mostrar la información. Si eres demasiado paranoico, puedes eliminar la historia y caché de tu navegador.</p></dd>
<dt>3. ¿Puedo <i>hackear</i> otra cuenta de usuario?</dt>
	<dd><p>Um, no. Pero puedes ver las conversaciones guardadas en tu PC. Eso significa que puedes usar Yammy (en cierta medida) para monitorizar a tus hijos activando el almacenamiento de sus conversaciones para ello.</p>
	<p>A falta de pan, buenas son tortas. Como diría el padre de Calvin, eso ayuda a fortalecer el carácter. Pero si lo que realmente buscas es monitorizar las conversaciones dentro de una red, echale un vistazo a <a href="http://imsniffer.sourceforge.net/" title="im sniffer">Im-Sniffer</a>.</p></dd>
<dt>4. Cuando trato de iniciar Yammy me sale el siguiente mensaje: 'The application failed to initialize properly'.</dt>
	<dd><p>Debes haberte bajado e instalado <a href="http://www.microsoft.com/downloads/details.aspx?FamilyID=0856eacb-4362-4b0d-8edd-aab15c5e04f5&displaylang=en" title=".net 2.0 runtime">.NET 2.0 Runtime</a>.</p></dd>
<dt>5. Cuando trato de realizar una busqueda, obtengo un error de "No Index Found".</dt>
	<dd><p>Todavía no se ha creado el índice. Vuelve a intentarlo al de un rato. Si el error persiste, si que hay un problema. Visita los <a href="http://yammy.sf.net/forums" title="visit forums">foros</a> o <a href="mailto:pravinp@gmail.com" title="email me">envíame un correo</a>.</p></dd>
<dt>6. Al imprimir, se ve diferente.</dt>
	<dd><p>Se usa un estilo distinto para la impresión. Todos esos imaginativos colores y el fondo desaparecen en la impresión. Si por alguna razón, quisieses imprimirlos, copia el contenido del fichero <code>display.css</code> a <code>print.css</code>.</p></dd>
<dt>7. ¿Por qué todos los iconos de los distintos usuarios remotos son iguales?</dt>
	<dd><p>Aún no he logrado saber como obtener los iconos de los usuarios remotos. Por eso de momento todos tienen este icono:<br /><img src="/images/generic.png" width="48" height="48" /></p></dd>
<dt>8. No soy un usuario remoto y... ¡mi icono es el mismo!</dt>
	<dd><p>Eso es porque no has puesto una imagen como avatar. Ponla y prueba de nuevo.</p></dd>
</dl>

