<h2>Getting the source</h2>
<p>You can download the source code to various versions from the <a href="/download/" title="Download Yammy">download page</a>. For those with svn,<br>
<code> svn co https://yammy.svn.sourceforge.net/svnroot/yammy yammy</code></p>

<h2>Compiling</h2>

<p>Different versions of Yammy were built using different tools. For Yammy 0.4 and lower, you will need a copy of Dev-C++. Yammy 0.5-0.7 were compiled in .NET 1.0 using SharpDevelop. Version 0.9 onwards use Visual C# Express Edition.</p>

<h2>Translations</h2>
<p>Yammy wouldn't be the same if it didn't support multiple languages. Thanks to the translators, Yammy is available in 15 languages (and growing). Would you like to <a href="/dev/translating-yammy.php" title="Translating Yammy">translate Yammy</a> too?</p>

