<h1>What is Yammy</h1>
<p>Yammy is an app that allows you to decode and view not only yours but other's archived YM conversations.</p>

<p>You don't even need a password!</p>

<h1>Features:</h1>
<ul>
    <li> Decode and view archived conversations and mobile messages.</li>
    <li> Control archiving for all users on your PC.</li>
    <li> Batch decode entire directories.</li>
    <li> Use Yammy in your language. Yammy is currently available in:<br />
      English, Basque, Catalan, Dutch, French, Norwegian, Romanian, Spanish, Swedish, Tamil and Turkish.</li>
</ul>

