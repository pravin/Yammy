	<dd><p>Inprimatzeko estilo desberdin bat erabili dut. Estilo horretan kolore irudimentsuak eta atzeko planoa desagertu egin dira. Edozein arrazoiagatik, inprimatu nahi baldin badituzu kolore irudimentsu horiek, kopiatu <code>display.css</code> fitxategiaren edukia <code>print.css</code>-ra.</p></dd>
