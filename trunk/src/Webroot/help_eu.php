<h2>FAQ</h2>
<dl>
<dt>1. Zergatik EI berria?</dt>
	<dd><p><b>Lehenengo arrazoia:</b> Zerbait desberdina egiteko beharra nuen. <i>Web-UIa is in. Windows GUI is out.</i></p>
	<p><b>Bigarren arrazoia:</b> Praktikoagoa da. Nire lanean <i>bakend stuf</i>a idazten. Programatzerakoan, GUIak baino liburutegi askoz gehiago egin ditut nire bizitzan zehar. Gainera, Windowsen programazioak, edo arazo honetarako, GUI programazioak ez du erraztasunik ematen logika eta aurkezpena bereizteko. Luzaroan ausnartu eta gero, zein izan da nire konponbidea? Hau.</p></dd>
<dt>2. Zergatik erabili behar dut nire nabigatzailea? Nire datuak seguru al daude?</dt>
	<dd><p>Ez zara internetera helduko. Lan guztia zure ordenagailuan egingo duzu. Yammy web serbitzari arin bat exekutatzen du. Horrela, datuak zure ordenagailuan egongo dira soilik denbora guztian zehar. Erabiltzaileak <b>ezin dute</b> beste ordenagailu batetik Yammyra heldu.</p>
	<p>Eta nabigatzailea? Datuak erakuzteko erabili ohi da. Benetan paranoikoa baldin bazara, nabigatzailearen historia eta cachea ezabatu ditzakezu.</p></dd>
