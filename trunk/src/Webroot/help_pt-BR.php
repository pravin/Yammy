<h2>Perguntas Freqüentes</h2>
<dl>
<dt>1. Por que fazer uma nova interface?</dt>
	<dd><p><b>Motivo 1:</b> Eu queria fazer algo diferente e <i>cool</i>. Interfaces web estão na moda. Interfaces Windows não. w00t!</p>
    <p><b>Motivo 2:</b> Assim é mais prático. Meu trabalho envolve programar coisas de backend. Eu programei mais bibliotecas do que interfaces. Programação Windows e GUI não tornam simples a tarefa de separar apresentação e lógica. Por <i>muito</i> tempo, eu estive procurando uma maneira de resolver esse problema. Resultado final? Isso. Ainda não está pronto ainda, mas está quase.</p></dd>
<dt>2. Por que eu previso usar o navegador? Meus dados estão seguros?</dt>
	<dd><p>Você não está acessando a internet. Tudo é feito localmente. Yammy roda o seu próprio servidor, então seus dados estão restritos ao seu computador. Outros usuários não podem acessar o Yammy de um computador remoto.</p>
	<p>E o navegador? Ele é usado para exibir os dados. Se você estiver realmente preocupado, você pode deletar o histórico de navegação e os arquivos do cache.</p></dd>
<dt>3. Posso hackear outra conta?</dt>
	<dd><p>Não. Mas você pode ver as conversas arquivadas no seu computador. Isso significa que você pode usar o Yammy para monitorar seus filhos, bastando habilitar o arquivamento para eles.</p>
	<p>Se você está realmente procurando uma maneira de monitorar conversas de outras pessoas pela rede, dê uma olhada no programa <a href="http://imsniffer.sourceforge.net/" title="im sniffer">Im-Sniffer</a>.</p></dd>
<dt>4. Quando eu tento inicializar o Yammy, eu recebo a mensagem 'The application failed to initialize properly'.</dt>
	<dd><p>Você precisará fazer o download e instalar o <a href="http://www.microsoft.com/downloads/details.aspx?FamilyID=0856eacb-4362-4b0d-8edd-aab15c5e04f5&displaylang=en" title=".net 2.0 runtime">.NET 2.0 Runtime</a>.</p></dd>
<dt>5. Quando eu tento fazer uma busca, ocorre o erro "Índice não encontrado".</dt>
	<dd><p>Seu índice não foi criado ainda. Tente novamente depois de algum tempo. Se o erro permanecer, então há um problema. Para obter ajuda, acesse os <a href="http://yammy.sf.net/forums" title="acessar os fóruns">fóruns</a> ou <a href="mailto:pravinp@gmail.com" title="enviar email">me envie um email</a>.</p></dd>
<dt>6. Quando eu imprimo, a aparência é diferente.</dt>
	<dd><p>Existe uma folha de estilo diferente para impressão. Assim, as cores e o plano de fundo não são impressos. Se, por alguma razão, você quiser imprimir as cores e imagens, copie o conteúdo do arquivo <code>display.css</code> para o <code>print.css</code>.</p></dd>
<dt>7. Por que todos os ícones de usuários remotos são os mesmos?</dt>
	<dd><p>Eu ainda não descobri como obter os ícones de usuários remotos. Por enquanto todos eles se parecem com esse:<br /><img src="/images/generic.png"width="48" height="48" /></p></dd>
<dt>8. Eu não sou um usuário remoto, mas meu ícone é o mesmo!</dt>
	<dd><p>Isso porque você ainda não configurou uma imagem como avatar. Altere isso e tente novamente.</p></dd>
</dl>

<p>Se você ainda precisar de ajuda, <a href="http://yammy.sf.net/forums" title="visite os fóruns">visite os fóruns</a> e poste suas dúvidas.</p> 

