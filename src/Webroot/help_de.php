<h2>FAQ</h2>
<dl>
<dt>1. Warum die neue UI</dt>
	<dd><p><b>Grund 1:</b> Ich wollte mal was anderes, cooleres machen. Das Web-UI ist in. Windows GUI ist out. w00t!</p>
	<p><b>Grund 2:</b> Es ist praktikabler. Ich muss auch das Backend programmieren. Dafür habe ich mehr Bibliotheken geschrieben
	, als was am GUI getan zu haben. Bei Windows-Programmierung, oder besser gesagt, bei GUI-Programmierung ist es nicht einfach,
	zwischen Logik und Präsentation zu unterscheiden. Seit <i>langer</i> Zeit habe ich versucht, diese Problem zu lösen. Das hier 
	ist das Ergebnis. Es ist noch nicht ganz, aber sehr gut auf dem Weg.</p></dd>
<dt>2. Warum muss ich meinen Browser benutzen? Sind meine Daten sicher?</dt>
	<dd><p>Sie greifen nicht auf das Internet zu. Alles läuft auf dem lokalen Rechner ab. Yammy betreibt einen kleinen Webserver.
	Die Daten verbleiben auf dem lokalen PC. Benutzer können YammyX von einem Remote-PC <b>nicht</b>steuern.</p>
	<p>Der Browser dient nur der Datenanzeige. Sie können natürlich, um sicherzugehen, auch Cache und Verlauf löschen.</p></dd>
<dt>3. Kann ich einen anderen Account hacken?</dt>
	<dd><p>Äh, nein. Aber Sie könnnen sich die archivierten Unterhaltungen anschauen. Das heißt, dass Sie Yammy als eine Art Überwachung
	der Unterhaltungen Ihrer Kinder nutzen können.</p>
	<p>Naja, Sie haben nach Kuchen gefragt und ich biete ihnen Brot an. Calvin's Vater würde sagen, das helfe dem Charakter. Aber 
	wenn Sie eine Unterhaltungsüberwachungs-Software benötigen, probieren Sie <a href="http://imsniffer.sourceforge.net/" title="im sniffer">Im-Sniffer</a>.</p></dd>
<dt>4. Wenn ich Yammy starten will, erscheint die Fehlermeldung 'The application failed to initialize properly'.</dt>
	<dd><p>Sie müssen das <a href="http://www.microsoft.com/downloads/details.aspx?FamilyID=0856eacb-4362-4b0d-8edd-aab15c5e04f5&displaylang=en" title=".net 2.0 runtime">.NET 2.0 Runtime</a> downloaden und installieren.</p></dd>
<dt>5. Wenn ich etwas suche, erhalte ich den Fehler "Kein Index gefunden".</dt>
	<dd><p>Ihr Index wurde noch nicht erstellt. Versuchen Sie es später noch mal. Wenn der Fehler erneut auftritt, liegt ein Problem vor.
	Besuchen Sie das <a href="http://yammy.sf.net/forums" title="visit forums">Forum</a> oder <a href="mailto:pravinp@gmail.com" title="email me">schreiben Sie mir</a>.</p></dd>
<dt>6. Wenn ich etwas drucke, sieht alles anders aus.</dt>
	<dd><p>Das Stylesheet für Drucken ist anders. Deswegen verschwinden die Farben und der Hintergrund. Wenn Sie aber die tollen
	Farben auf Papier haben wollen, kopieren Sie den Inhalt von <code>display.css</code> in <code>print.css</code>.</p></dd>
<dt>7. Warum schauen die Remote-Icons gleich aus?</dt>
	<dd><p>Ich habe noch nicht herausgefunden, wie die Icons zu holen sind. Im Moment sehen die Icons so aus:<br /><img src="/images/generic.png"width="48" height="48" /></p></dd>
<dt>8. Ich bin kein Remote-User. Mein Icon sieht aber genauso aus!</dt>
	<dd><p>Sie haben noch kein eigenes Icon eingestellt. Tun Sie das und versuchen es nochmal.</p></dd>
</dl>

