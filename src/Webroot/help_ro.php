<h2>FAQ</h2>
<dl>
<dt>1. Ce e cu noua interfata?</dt>
	<dd><p><b>Motivul 1:</b> Am vrut sa fac ceva diferit si cool. Interfata Web -Asa da. Interfata Windows -Asa nu. w00t!</p>
	<p><b>Motivul 2:</b> Este mai practic.Munca mea implica programare logica. Am scris mia multe librarii decat intergete grafice. Programarea in windows, si in aceeasi masura, programarea interfetei nu separa eficient logica de infatisare. Pentru o <i>foarte</i> lunga perioada de timp, am incercat sa gasesc o rezolvare. Rezultatul ? Ceea ce vezi. Inca nu am terminat de lucru dar e aproape de final.</p></dd>
<dt>2. De ce trebuie sa folosesc browser-ul? Sunt in siguranta?</dt>
	<dd><p>Nu accesezi nimic prin internet. Totul e local . Yammy ruleaza un mic server web . Deci datele sunt doar pe PC-ul tau. Userii <b>nu pot</b> accesa Yammy de pe alt PC.</p>
	<p>Si browser-ul? E folosit pentru afisarea datelor . Daca traiesti cu frica ca ai putea pati ceva , poti sterge fisierele cache precum si istoria .</p></dd>
<dt>3. Pot sa accesez un alt cont?</dt>
	<dd><p>Aa, nu. Dar poti vedea toate conversatiile ce au avut loc de pe PC-ul tau. Asta inseamna ca poti folosi Yammy (intr-un fel) sa monitorizezi conversatiile copiilor tai activandu-le Arhivarea mesajelor.</p>
	<p>Vezi, tu ai cerut ceva eu ti-am dat altceva . Cum ar spune tatal lui Calvin, asa se formeaza caracterul. Daca chiar cauti sa monitorizezi conversatiile din retea, incearca <a href="http://imsniffer.sourceforge.net/" title="im sniffer">Im-Sniffer</a>.</p></dd>
<dt>4. Cand incerc sa pornesc Yammy primesc o eroarea : 'The application failed to initialize properly'.</dt>
	<dd><p>Daca primesti aceasta eroare downloadeaza si instaleaza <a href="http://www.microsoft.com/downloads/details.aspx?FamilyID=0856eacb-4362-4b0d-8edd-aab15c5e04f5&displaylang=en" title=".net 2.0 runtime">.NET 2.0 Runtime</a>.</p></dd>
<dt>5. Cand incerc sa caut primesc eroarea : "Nu s-a gasit index!.</dt>
	<dd><p>Inca nu s-a terminat creearea index-ului. Mai asteapta cateva minute. daca eroarea persista dupa mai mult timp inseaman ca este o problema. Viziteaza <a href="http://yammy.sf.net/forums" title="visit forums">forum-ul</a> sau <a href="mailto:pravinp@gmail.com" title="email me">trimite-mi un mail</a>.</p></dd>
<dt>6. Cand imprim arata altfel.</dt>
	<dd><p>Stilul imprimarii este diferit. Toata grafica si culorile din fundal sunt scoase. Daca din vreun motiv vrei sa imprimi si culorile si grafica, copie continutul filei <code>display.css</code> in fila <code>print.css</code>.</p></dd>
